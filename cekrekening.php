      <h2>Cek Rekening</h2>
      <img src="logo/crek.png" alt="Banner Cek Rekening" class="banner-aduan">
      <p>CekRekening.id adalah Kanal Layanan Pencegahan Tindak Pidana resmi dari <b>Kementerian Komunikasi dan Digital Republik Indonesia</b> yang difungsikan sebagai portal untuk melakukan pelaporan dan pencarian rekening bank dan e-wallet terindikasi tindak pidana. Selain dua hal tersebut, cekrekening.id juga memiliki fitur untuk pemberian whitelist rekening yang terpercaya yang diajukan oleh masyarakat.</p>

      <p>Masyarakat dapat melaporkan rekening-rekening yang terindikasi kejahatan seperti penipuan online, investasi fiktif, pemerasan, prostitusi online, judi online, terorisme, dan lain-lain.</p>

      <p>Rekening dan akun e-wallet yang dilaporkan masyarakat pada akhirnya akan dapat dimanfaatkan oleh pengguna lain melalui fitur pencarian rekening atau akun e-wallet terindikasi tindak pidana. Model inilah yang disebut dengan sistem crowdsourcing atau pemanfaatan sumber daya kerumunan. Rekening atau akun e-wallet terindikasi tindak pidana yang sudah terverifikasi oleh verifikator bisa diakses kembali oleh masyarakat untuk mencegah berulangnya pemanfaatan rekening atau akun e-wallet tersebut sebagai sarana kejahatan.</p>

      <p>Untuk memastikan adanya sistem kendali yang fair atas laporan, portal cekrekening.id juga memiliki fitur normalisasi bagi pemilik rekening yang merasa rekeningnya tidak terkait tindak pidana namun masuk dalam laporan yang terverifikasi. Proses normalisasi dilakukan dengan pendekatan Online Dispute Resolution, yakni menghadirkan pelapor dan terlapor secara online untuk kemudian ditentukan status pelaporannya berdasarkan bukti yang diajukan dari kedua belah pihak.</p>

      <p>Fitur whitelist rekening dalam portal cekrekening.id dimanfaatkan oleh para UMKM dan pedagang atau pemberi jasa online perseorangan untuk mendapatkan centang biru dengan QR code yang jika dipindai akan merujuk pada web site resmi cekrekening.id. Hal tersebut mengindikasikan bahwa rekening yang ada dalam whitelist adalah rekening terpercaya untuk masyarakat melakukan transaksi. Proses pemberian centang biru dan QR code atas rekening dilakukan melalui verifikasi faktual atas pengajuan yang dilakukan oleh pemohon.</p>

      <h3>Catatan Penting:</h3>
      <p>Layanan CekRekening.id sepenuhnya diselenggarakan oleh Kementerian Komunikasi dan Digital Republik Indonesia. Dinas Komunikasi dan Informatika Kabupaten Lamongan hanya berperan dalam menyebarluaskan informasi agar masyarakat lebih terlindungi dari potensi tindak pidana digital.</p>

